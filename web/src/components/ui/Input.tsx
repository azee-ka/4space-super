import React, { useState, forwardRef } from 'react';

interface InputProps extends Omit<React.InputHTMLAttributes<HTMLInputElement>, 'onChange'> {
  label?: string;
  error?: string;
  helperText?: string;
  icon?: React.ReactNode;
  iconPosition?: 'left' | 'right';
  onChange?: (e: React.ChangeEvent<HTMLInputElement>) => void;
}

export const Input = forwardRef<HTMLInputElement, InputProps>(
  (
    {
      label,
      error,
      helperText,
      icon,
      iconPosition = 'left',
      type = 'text',
      className = '',
      disabled,
      ...props
    },
    ref
  ) => {
    const [showPassword, setShowPassword] = useState(false);
    const [isFocused, setIsFocused] = useState(false);

    const isPassword = type === 'password';
    const inputType = isPassword && showPassword ? 'text' : type;

    return (
      <div className="w-full mb-5">
        {label && (
          <label className="block text-sm font-semibold text-gray-700 dark:text-gray-300 mb-2 ml-1">
            {label}
          </label>
        )}
        
        <div className="relative group">
          <div
            className={`
              relative flex items-center
              rounded-xl overflow-hidden
              transition-all duration-300
              ${isFocused ? 'ring-2 ring-primary-500/50 dark:ring-primary-400/50' : ''}
              ${error ? 'ring-2 ring-red-500/50' : ''}
            `}
          >
            {icon && iconPosition === 'left' && (
              <div className="absolute left-4 text-gray-400 dark:text-gray-500">
                {icon}
              </div>
            )}
            
            <input
              ref={ref}
              type={inputType}
              className={`
                w-full px-4 py-3.5
                ${icon && iconPosition === 'left' ? 'pl-12' : ''}
                ${(icon && iconPosition === 'right') || isPassword ? 'pr-12' : ''}
                glass
                text-gray-900 dark:text-white
                placeholder-gray-400 dark:placeholder-gray-500
                focus:outline-none
                transition-all duration-300
                disabled:opacity-50 disabled:cursor-not-allowed
                ${className}
              `}
              onFocus={() => setIsFocused(true)}
              onBlur={() => setIsFocused(false)}
              disabled={disabled}
              {...props}
            />
            
            {isPassword && (
              <button
                type="button"
                onClick={() => setShowPassword(!showPassword)}
                className="absolute right-4 text-gray-400 hover:text-gray-600 dark:hover:text-gray-300 transition-colors"
                tabIndex={-1}
              >
                {showPassword ? (
                  <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.88 9.88l-3.29-3.29m7.532 7.532l3.29 3.29M3 3l3.59 3.59m0 0A9.953 9.953 0 0112 5c4.478 0 8.268 2.943 9.543 7a10.025 10.025 0 01-4.132 5.411m0 0L21 21" />
                  </svg>
                ) : (
                  <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" />
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z" />
                  </svg>
                )}
              </button>
            )}
            
            {icon && iconPosition === 'right' && !isPassword && (
              <div className="absolute right-4 text-gray-400 dark:text-gray-500">
                {icon}
              </div>
            )}
          </div>
          
          {/* Animated underline */}
          <div
            className={`
              h-0.5 w-0 bg-gradient-to-r from-primary-500 to-primary-600
              transition-all duration-300 mx-auto
              ${isFocused ? 'w-full' : 'w-0'}
            `}
          />
        </div>
        
        {error && (
          <p className="mt-2 text-sm text-red-500 dark:text-red-400 ml-1 flex items-center gap-1 animate-fade-in">
            <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 20 20">
              <path fillRule="evenodd" d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7 4a1 1 0 11-2 0 1 1 0 012 0zm-1-9a1 1 0 00-1 1v4a1 1 0 102 0V6a1 1 0 00-1-1z" clipRule="evenodd" />
            </svg>
            {error}
          </p>
        )}
        
        {helperText && !error && (
          <p className="mt-2 text-sm text-gray-500 dark:text-gray-400 ml-1">
            {helperText}
          </p>
        )}
      </div>
    );
  }
);

Input.displayName = 'Input';